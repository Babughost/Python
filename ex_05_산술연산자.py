# 산술 연산자


a = 7
b = 2

print('{} + {} = {}'.format(a, b, a + b))       # 덧셈
print('{} - {} = {}'.format(a, b, a - b))       # 뺄셈
print('{} * {} = {}'.format(a, b, a * b))       # 곱셈
print('{} ** {} = {}'.format(a, b, a ** b))     # 거듭제곱
print('{} / {} = {}'.format(a, b, a / b))       # 나눗셈
print('{} // {} = {}'.format(a, b, a // b))     # 몫
print('{} % {} = {}'.format(a, b, a % b))       # 나머지

print()

print(f'{a} + {b} = {a + b}')
print(f'{a} - {b} = {a - b}')
print(f'{a} * {b} = {a * b}')
print(f'{a} ** {b} = {a ** b}')
print(f'{a} / {b} = {a / b}')
print(f'{a} // {b} = {a // b}')
print(f'{a} % {b} = {a % b}')